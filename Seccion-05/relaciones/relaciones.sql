-- Relaciones

-- Tipos de relaciones entre tablas

-- 1. Relación uno a uno - one to one
-- 2. Relación uno a muchos - one to many
-- 3. Relación muchos a muchos - many to many
-- 4. Relaciones a si misma - self joining relationship

-- Relación uno a uno - one to one

-- Ejemplo: Una persona tiene un pasaporte
-- Ejemplo: Un usuario tiene un perfil
-- Ejemplo: Un usuario tiene una dirección de envío

-- Relación uno a muchos - one to many

-- Ejemplo: Un usuario tiene muchas direcciones de envío
-- Ejemplo: Un usuario tiene muchas órdenes de compra
-- Ejemplo: Un usuario tiene muchas tarjetas de crédito

-- Relación muchos a muchos - many to many

-- Ejemplo: Un usuario tiene muchos productos en su carrito
-- Ejemplo: Un usuario tiene muchos productos en su wishlist
-- Ejemplo: Un usuario tiene muchos productos en su historial de compras

-- Relaciones a si misma - self joining relationship

-- Ejemplo: Un usuario tiene muchos amigos
-- Ejemplo: Un usuario tiene muchos seguidores
-- Ejemplo: Un usuario tiene muchos seguidos

